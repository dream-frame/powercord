const { AsyncComponent } = require('powercord/components');
const { React, getModuleByDisplayName } = require('powercord/webpack');

const Base = require('./Base');
const Slider = AsyncComponent.from(getModuleByDisplayName('Slider'));

module.exports = class SliderItem extends Base {
  render () {
    const { props } = this;
    this.component = React.createElement('div', {
      className: this.classes.itemSlider
    }, React.createElement('span', {
      className: this.classes.label,
      style: props.markers && { marginBottom: '16px' }
    }, props.name), this.renderSlider());

    return super.render();
  }

  renderSlider () {
    return React.createElement(Slider, {
      mini: true,
      className: this.classes.slider,
      fillStyles: this.props.color && { backgroundColor: this.props.color },
      equidistant: typeof this.props.markers !== 'undefined',
      stickToMarkers: typeof this.props.markers !== 'undefined',
      ...this.props
    });
  }
};
