const { getModuleByDisplayName, modal } = require('powercord/webpack');
const AsyncComponent = require('./AsyncComponent');

require('fs')
  .readdirSync(__dirname)
  .filter(file => file !== 'index.js')
  .forEach(filename => {
    const moduleName = filename.split('.')[0];
    exports[moduleName] = require(`${__dirname}/${filename}`);
  });

Object.assign(exports, {
  Confirm: AsyncComponent.from(getModuleByDisplayName('ConfirmModal')),
  Modal: AsyncComponent.from(getModuleByDisplayName('DeprecatedModal'))
});

// Re-export module properties
(async () => {
  const Modal = await getModuleByDisplayName('DeprecatedModal', true, true);
  [ 'Header', 'Footer', 'Content', 'ListContent', 'CloseButton', 'Sizes' ].forEach(prop => exports.Modal[prop] = Modal[prop]);
  exports.Confirm.defaultProps = {
    transitionState: 1,
    onClose: modal.pop
  };
})();
